package Assignments;

public class Assignment08 {

	public static void main(String[] args) {

		int days = 3;

		switch (days) {

		case 1:
			System.out.println("Friday");
			break;			
		case 2:
			System.out.println("Saturday");
			break;
		case 3:
			System.out.println("Sunday");
			break;
		default :
				System.out.println("Monday");

		}

	}

}
