package Assignments;

//Write a Program on nested if Else Block_Case
public class Assignment07 {

	public static void main(String[] args) {

		int age = 17;

		if (age > 18) {

			System.out.println("Eligible for Voting in India");

		} else if (age >= 18) {

			System.out.println("Also Eligible for Voting in India");

		} else if (age < 18) {

			System.out.println("Not Eligible for Voting in India");
		}

	}

}
